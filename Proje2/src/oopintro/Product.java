package oopintro;

public class Product {
	int id;
	String name;
	double priceStock;
	String discount;
	
	public Product() {
		
	}

	public Product(int id, String name, double priceStock, String discount) {
		super();
		this.id = id;
		this.name = name;
		this.priceStock = priceStock;
		this.discount = discount;
	}
	
}
